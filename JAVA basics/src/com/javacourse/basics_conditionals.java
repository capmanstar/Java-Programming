package com.javacourse;

public class basics_conditionals {
    public static void main(String [] args){

        if(false){
            System.out.println("Hello world");
        }

        if(true){
            System.out.println("Hello world");
        }

        int i=1;

        while(i!=5){
            System.out.println(i);
            i++;
        }

        for(int n=0; n<=100; n++){
            System.out.println(n);
        }
    }
}
